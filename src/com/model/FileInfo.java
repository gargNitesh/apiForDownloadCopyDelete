package com.model;

public class FileInfo {

	String originalFileLoc;
	String copiedFileLoc;
	
	public String getOriginalFileLoc() {
		return originalFileLoc;
	}
	public void setOriginalFileLoc(String originalFileLoc) {
		this.originalFileLoc = originalFileLoc;
	}
	public String getCopiedFileLoc() {
		return copiedFileLoc;
	}
	public void setCopiedFileLoc(String copiedFileLoc) {
		this.copiedFileLoc = copiedFileLoc;
	}
	
}
